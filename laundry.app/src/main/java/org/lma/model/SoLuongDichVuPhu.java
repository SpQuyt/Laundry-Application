package org.lma.model;

public class SoLuongDichVuPhu {
	private int aoKhoacTo;
	private int aoKhoacVua;
	private int aoKhoacNho;
	private int chanTo;
	private int chanVua;
	private int chanNho;
	
	public int getAoKhoacToSoLuong() {
		return aoKhoacTo;
	}
	
	public int getAoKhoacVuaSoLuong() {
		return aoKhoacVua;
	}
	
	public int getAoKhoacNhoSoLuong() {
		return aoKhoacNho;
	}
	
	public void setAoKhoacToSoLuong(int aoKhoacTo) {
		this.aoKhoacTo = aoKhoacTo;
	}
	
	public void setAoKhoacVuaSoLuong(int aoKhoacVua) {
		this.aoKhoacVua = aoKhoacVua;
	}
	
	public void setAoKhoacNhoSoLuong(int aoKhoacNho) {
		this.aoKhoacNho = aoKhoacNho;
	}
	
	
	public int getChanToSoLuong() {
		return chanTo;
	}
	
	public int getChanVuaSoLuong() {
		return chanVua;
	}
	
	public int getChanNhoSoLuong() {
		return chanNho;
	}
	
	public void setChanToSoLuong(int chanTo) {
		this.chanTo = chanTo;
	}
	
	public void setChanVuaSoLuong(int chanVua) {
		this.chanVua = chanVua;
	}
	
	public void setChanNhoSoLuong(int chanNho) {
		this.chanNho = chanNho;
	}
}
